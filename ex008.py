#Escreva um programa que leia um vlaor em metros e converta.
m = float(input('Digite um valor em metros(M): '))

print(f'{m}m em milímetros é {m * 1000}mm')
print(f'{m}m em centímetros é {m * 100}cm')
print(f'{m}m em decímetro é {m * 10}dm')
print(f'{m}m em decâmetro é {m * 0.1}dam')
print(f'{m}m em hectômetro é {m * 0.01}hm')
print(f'{m}m em quilômetro é {m * 0.001}km')
